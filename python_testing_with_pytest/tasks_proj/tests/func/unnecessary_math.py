# unnecessary_math.py

def multiply(a, b):
    """
    Multiply two values. If a is a string and b is an int, repeats the string.

    Examples:
    >>> um.multiply(4, 3)
    12
    >>> um.multiply('a', 3)
    'aaa'
    """
    return a * b

def divide(a, b):
    """
    Returns a divided by b.

    Examples:
    >>> um.divide(10, 5)
    2.0
    >>> um.divide(7, 2)
    3.5
    """
    return a / b

def subtract(a, b):
    """
    Retruns a subract by b.

    Examples:
    >>> um.subtract(10, 5)
    5
    >>> um.subtract(20, 10)
    10
    """
    return a - b
